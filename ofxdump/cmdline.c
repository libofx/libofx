/**
 * @brief updates an option
 * @param field the generic pointer to the field to update
 * @param orig_field the pointer to the orig field
 * @param field_given the pointer to the number of occurrence of this option
 * @param prev_given the pointer to the number of occurrence already seen
 * @param value the argument for this option (if null no arg was specified)
 * @param possible_values the possible values for this option (if specified)
 * @param default_value the default value (in case the option only accepts fixed values)
 * @param arg_type the type of this option
 * @param check_ambiguity @see cmdline_parser_params.check_ambiguity
 * @param override @see cmdline_parser_params.override
 * @param no_free whether to free a possible previous value
 * @param multiple_option whether this is a multiple option
 * @param long_opt the corresponding long option
 * @param short_opt the corresponding short option (or '-' if none)
 * @param additional_error possible further error specification
 */
static
int update_arg(void *field, char **orig_field,
               unsigned int *field_given, unsigned int *prev_given, 
               char *value, const char *possible_values[],
               const char *default_value,
               cmdline_parser_arg_type arg_type,
               int check_ambiguity, int override,
               int no_free, int multiple_option,
               const char *long_opt, char short_opt,
               const char *additional_error)
{
  char *stop_char = 0;
  const char *val = value;
  int found;
  char **string_field;
  FIX_UNUSED (field);

  stop_char = 0;
  found = 0;

  if (!multiple_option && prev_given && (*prev_given || (check_ambiguity && *field_given)))
    {
      if (short_opt != '-')
        fprintf (stderr, "%s: `--%s' (`-%c') option given more than once%s\n", 
               package_name, long_opt, short_opt,
               (additional_error ? additional_error : ""));
      else
        fprintf (stderr, "%s: `--%s' option given more than once%s\n", 
               package_name, long_opt,
               (additional_error ? additional_error : ""));
      return 1; /* failure */
    }

  FIX_UNUSED (default_value);
    
  if (field_given && *field_given && ! override)
    return 0;
  if (prev_given)
    (*prev_given)++;
  if (field_given)
    (*field_given)++;
  if (possible_values)
    val = possible_values[found];

  switch(arg_type) {
  case ARG_FLAG:
    *((int *)field) = !*((int *)field);
    break;
  case ARG_STRING:
    if (val) {
      string_field = (char **)field;
      if (!no_free && *string_field)
        free (*string_field); /* free previous string */
      *string_field = gengetopt_strdup (val);
    }
    break;
  default:
    break;
  };

	FIX_UNUSED(stop_char);
	
  /* store the original value */
  switch(arg_type) {
  case ARG_NO:
  case ARG_FLAG:
    break;
  default:
    if (value && orig_field) {
      if (no_free) {
        *orig_field = value;
      } else {
        if (*orig_field)
          free (*orig_field); /* free previous string */
        *orig_field = gengetopt_strdup (value);
      }
    }
  };

  return 0; /* OK */
}


int
cmdline_parser_internal (
  int argc, char **argv, struct gengetopt_args_info *args_info,
                        struct cmdline_parser_params *params, const char *additional_error)
{
  int c;	/* Character of the parsed option.  */

  int error_occurred = 0;
  struct gengetopt_args_info local_args_info;
  
  int override;
  int initialize;
  int check_required;
  int check_ambiguity;
  
  package_name = argv[0];
  
  /* TODO: Why is this here? It is not used anywhere. */
  override = params->override;
  FIX_UNUSED(override);

  initialize = params->initialize;
  check_required = params->check_required;

  /* TODO: Why is this here? It is not used anywhere. */
  check_ambiguity = params->check_ambiguity;
  FIX_UNUSED(check_ambiguity);

  if (initialize)
    cmdline_parser_init (args_info);

  cmdline_parser_init (&local_args_info);

  optarg = 0;
  optind = 0;
  opterr = params->print_errors;
  optopt = '?';

  while (1)
    {
      int option_index = 0;

      static struct option long_options[] = {
        { "help",	0, NULL, 'h' },
        { "version",	0, NULL, 'V' },
        { "import-format",	1, NULL, 'f' },
        { "list-import-formats",	0, NULL, 0 },
        { "msg_parser",	0, NULL, 0 },
        { "msg_debug",	0, NULL, 0 },
        { "msg_warning",	0, NULL, 0 },
        { "msg_error",	0, NULL, 0 },
        { "msg_info",	0, NULL, 0 },
        { "msg_status",	0, NULL, 0 },
        { 0,  0, 0, 0 }
      };

      c = getopt_long (argc, argv, "hVf:", long_options, &option_index);

      if (c == -1) break;	/* Exit from `while (1)' loop.  */

      switch (c)
        {
        case 'h':	/* Print help and exit.  */
          cmdline_parser_print_help ();
          cmdline_parser_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'V':	/* Print version and exit.  */
          cmdline_parser_print_version ();
          cmdline_parser_free (&local_args_info);
          exit (EXIT_SUCCESS);

        case 'f':	/* Force the file format of the file(s) specified.  */
        
        
          if (update_arg( (void *)&(args_info->import_format_arg), 
               &(args_info->import_format_orig), &(args_info->import_format_given),
              &(local_args_info.import_format_given), optarg, 0, "AUTODETECT", ARG_STRING,
              check_ambiguity, override, 0, 0,
              "import-format", 'f',
              additional_error))
            goto failure;
        
          break;

        case 0:	/* Long option with no short option */
          /* List available import file formats 'import-format' command.  */
          if (strcmp (long_options[option_index].name, "list-import-formats") == 0)
          {
          
          
            if (update_arg( 0 , 
                 0 , &(args_info->list_import_formats_given),
                &(local_args_info.list_import_formats_given), optarg, 0, 0, ARG_NO,
                check_ambiguity, override, 0, 0,
                "list-import-formats", '-',
                additional_error))
              goto failure;
          
          }
          /* Output file parsing messages.  */
          else if (strcmp (long_options[option_index].name, "msg_parser") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->msg_parser_flag), 0, &(args_info->msg_parser_given),
                &(local_args_info.msg_parser_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "msg_parser", '-',
                additional_error))
              goto failure;
          
          }
          /* Output messages meant for debugging.  */
          else if (strcmp (long_options[option_index].name, "msg_debug") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->msg_debug_flag), 0, &(args_info->msg_debug_given),
                &(local_args_info.msg_debug_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "msg_debug", '-',
                additional_error))
              goto failure;
          
          }
          /* Output warning messages about abnormal conditions and unknown constructs.  */
          else if (strcmp (long_options[option_index].name, "msg_warning") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->msg_warning_flag), 0, &(args_info->msg_warning_given),
                &(local_args_info.msg_warning_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "msg_warning", '-',
                additional_error))
              goto failure;
          
          }
          /* Output error messages.  */
          else if (strcmp (long_options[option_index].name, "msg_error") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->msg_error_flag), 0, &(args_info->msg_error_given),
                &(local_args_info.msg_error_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "msg_error", '-',
                additional_error))
              goto failure;
          
          }
          /* Output informational messages about the progress of the library.  */
          else if (strcmp (long_options[option_index].name, "msg_info") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->msg_info_flag), 0, &(args_info->msg_info_given),
                &(local_args_info.msg_info_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "msg_info", '-',
                additional_error))
              goto failure;
          
          }
          /* Output status messages.  */
          else if (strcmp (long_options[option_index].name, "msg_status") == 0)
          {
          
          
            if (update_arg((void *)&(args_info->msg_status_flag), 0, &(args_info->msg_status_given),
                &(local_args_info.msg_status_given), optarg, 0, 0, ARG_FLAG,
                check_ambiguity, override, 1, 0, "msg_status", '-',
                additional_error))
              goto failure;
          
          }
          
          break;
        case '?':	/* Invalid option.  */
          /* `getopt_long' already printed an error message.  */
          goto failure;

        default:	/* bug: option not considered.  */
          fprintf (stderr, "%s: option unknown: %c%s\n", CMDLINE_PARSER_PACKAGE, c, (additional_error ? additional_error : ""));
          abort ();
        } /* switch */
    } /* while */



	FIX_UNUSED(check_required);

  cmdline_parser_release (&local_args_info);

  if ( error_occurred )
    return (EXIT_FAILURE);

  if (optind < argc)
    {
      int i = 0 ;
      int found_prog_name = 0;
      /* whether program name, i.e., argv[0], is in the remaining args
         (this may happen with some implementations of getopt,
          but surely not with the one included by gengetopt) */

      i = optind;
      while (i < argc)
        if (argv[i++] == argv[0]) {
          found_prog_name = 1;
          break;
        }
      i = 0;

      args_info->inputs_num = argc - optind - found_prog_name;
      args_info->inputs =
        (char **)(malloc ((args_info->inputs_num)*sizeof(char *))) ;
      while (optind < argc)
        if (argv[optind++] != argv[0])
          args_info->inputs[ i++ ] = gengetopt_strdup (argv[optind-1]) ;
    }

  return 0;

failure:
  
  cmdline_parser_release (&local_args_info);
  return (EXIT_FAILURE);
}
